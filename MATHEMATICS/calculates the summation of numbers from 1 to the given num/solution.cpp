
int summation(int num)
{
    return num * (num + 1) / 2;
}
/*
Explanation:
- This function calculates the sum of numbers from 1 to 'num', inclusive.
- The formula used is based on a mathematical concept known as the sum of an arithmetic series.
- By multiplying 'num' by 'num + 1' and dividing the result by 2, we obtain the sum of the series.
- For example, if 'num' is 5, the calculation is '(5 * (5 + 1)) / 2 = (5 * 6) / 2 = 30 / 2 = 15', which represents the sum of numbers from 1 to 5.
*/


